export function getPromptsForText(text) {
  const mode = detectParseMode(text)
  if (mode === 'vocabulary') {
    return {
      mode,
      systemPrompt: VOCABULARY_SYSTEM_PROMPT,
      userPrompt: buildVocabularyPrompt(text.trim()),
    }
  }
  return {
    mode: 'chunk',
    systemPrompt: CHUNK_SYSTEM_PROMPT,
    userPrompt: buildChunkPrompt(text.trim()),
  }
}
